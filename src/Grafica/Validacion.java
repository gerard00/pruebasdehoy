     private void Aceptar(){
         int pos=email.Buscar(txtLogin.getText(), txtcontra.getText());
        if(pos!=-1){
           if(email.tipo(pos)==1) {
            new VentanaAdministrador(email).setVisible(true);
            dispose();
            pack();
           }else{
            new VentanaUsuario(email).setVisible(true);
            dispose();
            pack();  
           }
           //int identificador=email.getMisEmpleados().get(pos).getTipoUsuario();
           //new Administrador(email,identificador).setVisible(true);
           //dispose();
           //pack();
        }
        else{
            JOptionPane.showMessageDialog(this, "DATOS INCORRECTOS");
            txtLogin.setText("");
            txtcontra.setText("");
            txtLogin.requestFocus();
            
        }
     }
